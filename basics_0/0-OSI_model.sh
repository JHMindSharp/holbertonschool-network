echo "1. Physical (Layer 1): Deals with the physical transmission of data over media."
echo "2. Data Link (Layer 2): Responsible for node-to-node data transfer and error detection."
echo "3. Network (Layer 3): Manages device addressing and routing of data."
echo "4. Transport (Layer 4): Ensures complete data transfer and error recovery."
echo "5. Session (Layer 5): Manages sessions with control structures for dialogue and data exchange."
echo "6. Presentation (Layer 6): Translates data between the application layer and the network."
echo "7. Application (Layer 7): The interface for the end-user operating in the network."

